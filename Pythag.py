print("Welcome to the pythagorean calculator, please enter your first number:")
a = int(input())
print("Please enter your second number:")
b = int(input())
asquared=pow(a,2)
bsquared=pow(b,2)
c=asquared+bsquared
print(c)
#this is the code for the pythagorean calculator
#asquared represents the square of the first number
#bsquared represents the square of the second number
#c represents the sum of the squares of the two numbers
#the formula for the pythagorean calculator is asquared+bsquared
#the pow command raises the first variable to the power of the second interger
